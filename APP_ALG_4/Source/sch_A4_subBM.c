BOOL SCHEDULER_CONTROL_Chk_BM_FREE_ALL_STYLE_4( int ch ) {
	int i;
	for ( i = 0 ; i < _SCH_PRM_GET_MODULE_SIZE( ch ) ; i++ ) {
		if ( _SCH_MODULE_Get_BM_WAFER( ch , i + 1 ) > 0 ) return FALSE;
	}
	return TRUE;
}

//==========================================================================================================================

BOOL SCHEDULER_CONTROL_Chk_BM_FULL_ALL_STYLE_4( int ch ) {
	int i;
	for ( i = 0 ; i < _SCH_PRM_GET_MODULE_SIZE( ch ) ; i++ ) {
		if ( _SCH_MODULE_Get_BM_WAFER( ch , i + 1 ) <= 0 ) return FALSE;
	}
	return TRUE;
}


//==========================================================================================================================

int SCHEDULER_CONTROL_Chk_BM_HAS_COUNT_STYLE_4( int ch , int checksts ) {
	int i , k = 0;
	for ( i = _SCH_PRM_GET_MODULE_SIZE( ch ) ; i >= 1 ; i-- ) {
		if ( _SCH_MODULE_Get_BM_WAFER( ch , i ) > 0 ) {
			if ( checksts == -1 ) {
				k++;
			}
			else {
				if ( _SCH_MODULE_Get_BM_STATUS( ch , i ) == checksts ) {
					k++;
				}
			}
		}
	}
	return k;
}

//==========================================================================================================================

int SCHEDULER_CONTROL_Chk_BM_FREE_COUNT_STYLE_4( int ch ) {
	int i , k = 0;
	for ( i = _SCH_PRM_GET_MODULE_SIZE( ch ) ; i >= 1 ; i-- ) {
		if ( _SCH_MODULE_Get_BM_WAFER( ch , i ) <= 0 ) k++;
	}
	return k;
}

